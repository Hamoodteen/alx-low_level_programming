#include "3-calc.h"
/**
 * op_add - f
 * @a: int
 * @b: int
 * Return: a + b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - f
 * @a: int
 * @b: int
 * Return: a - b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - f
 * @a: int
 * @b: int
 * Return: a * b
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - f
 * @a: int
 * @b: int
 * Return: a / b
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - f
 * @a: int
 * @b: int
 * Return: a % b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
